//point class from graph graphics hw
public class Point {

    int xcord;
    int ycord;
    String label;

    public Point(int xcord, int ycord, String label) {
        this.xcord = xcord;
        this.ycord = ycord;
        this.label = label;
    }

    public int getXCord() {
        return xcord;
    }

    public int getYCord() {
        return ycord;
    }
}
